package com.example.user.trucksales.Encapsuladoras;

public class BancoPropio {

    private String nrocta_banco;
    private String cod_banco;
    private String nom_banco;
    private String tipo_cta;
    private String cod_moneda;
    private String nom_moneda;


    public String getNrocta_banco() {
        return nrocta_banco;
    }

    public void setNrocta_banco(String nrocta_banco) {
        this.nrocta_banco = nrocta_banco;
    }

    public String getCod_banco() {
        return cod_banco;
    }

    public void setCod_banco(String cod_banco) {
        this.cod_banco = cod_banco;
    }

    public String getNom_banco() {
        return nom_banco;
    }

    public void setNom_banco(String nom_banco) {
        this.nom_banco = nom_banco;
    }

    public String getTipo_cta() {
        return tipo_cta;
    }

    public void setTipo_cta(String tipo_cta) {
        this.tipo_cta = tipo_cta;
    }

    public String getCod_moneda() {
        return cod_moneda;
    }

    public void setCod_moneda(String cod_moneda) {
        this.cod_moneda = cod_moneda;
    }

    public String getNom_moneda() {
        return nom_moneda;
    }

    public void setNom_moneda(String nom_moneda) {
        this.nom_moneda = nom_moneda;
    }
}
