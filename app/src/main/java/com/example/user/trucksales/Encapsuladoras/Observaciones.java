package com.example.user.trucksales.Encapsuladoras;

public class Observaciones {

    private String cod_motivo;
    private String fec_proxv;
    private String cod_tit;
    private String fec_doc;
    private String descripcion;
    private int cod_emp;
    private int num_viaje;

    public String getCod_motivo() {
        return cod_motivo;
    }

    public void setCod_motivo(String cod_motivo) {
        this.cod_motivo = cod_motivo;
    }

    public String getFec_proxv() {
        return fec_proxv;
    }

    public void setFec_proxv(String fec_proxv) {
        this.fec_proxv = fec_proxv;
    }

    public String getCod_tit() {
        return cod_tit;
    }

    public void setCod_tit(String cod_tit) {
        this.cod_tit = cod_tit;
    }

    public String getFec_doc() {
        return fec_doc;
    }

    public void setFec_doc(String fec_doc) {
        this.fec_doc = fec_doc;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getCod_emp() {
        return cod_emp;
    }

    public void setCod_emp(int cod_emp) {
        this.cod_emp = cod_emp;
    }

    public int getNum_viaje() {
        return num_viaje;
    }

    public void setNum_viaje(int num_viaje) {
        this.num_viaje = num_viaje;
    }
}
